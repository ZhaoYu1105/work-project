package net.kingsilk.qh.vote.api.voteApp.vote.voteRecord.dto;

public class VoteNotifyInfo {

    private String voteNickName;

    private String workerHeaderImgUrl;

    private String nickName;


    public String getVoteNickName() {
        return voteNickName;
    }

    public void setVoteNickName(String voteNickName) {
        this.voteNickName = voteNickName;
    }

    public String getWorkerHeaderImgUrl() {
        return workerHeaderImgUrl;
    }

    public void setWorkerHeaderImgUrl(String workerHeaderImgUrl) {
        this.workerHeaderImgUrl = workerHeaderImgUrl;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

}
